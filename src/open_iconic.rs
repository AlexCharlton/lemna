impl From<Icon> for char {
    fn from(i: Icon) -> char {
        unsafe { char::from_u32_unchecked(i as u32) }
    }
}

impl From<Icon> for String {
    fn from(i: Icon) -> String {
        String::from_utf16(&[i as u16]).unwrap()
    }
}
